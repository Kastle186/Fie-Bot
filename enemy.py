class Enemy:
    def __init__(self, name: str, HP: int, STR: int,
                 DEF: int, SPD: int, ATS: int, ADF: int,
                 level: int):
        self.name = name
        self.HP = HP
        self.STR = HP
        self.STR = STR
        self.DEF = DEF
        self.SPD = SPD
        self.ATS = ATS
        self.ADF = ADF
        self.level = level
        self.crafts = []
